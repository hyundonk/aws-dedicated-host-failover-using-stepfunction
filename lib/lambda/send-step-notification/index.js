/**
 * Build generic step notification
 */
function buildGenericStepMessage(step, hostId, instanceId, reservedHostId, timestamp, additionalInfo) {
  let message = `üìã MIGRATION STEP UPDATE\n\n`;
  message += `Migration workflow step: ${step}\n\n`;
  
  message += `üìã DETAILS:\n`;
  message += `‚Ä¢ Host ID: ${hostId}\n`;
  if (instanceId) message += `‚Ä¢ Instance ID: ${instanceId}\n`;
  if (reservedHostId) message += `‚Ä¢ Reserved Host: ${reservedHostId}\n`;
  message += `‚Ä¢ Timestamp: ${timestamp}\n`;
  
  if (Object.keys(additionalInfo).length > 0) {
    message += `\nüìù ADDITIONAL INFO:\n`;
    Object.entries(additionalInfo).forEach(([key, value]) => {
      message += `‚Ä¢ ${key}: ${value}\n`;
    });
  }
  
  message += `\nFor detailed status, check the AWS Step Functions console.`;
  
  return message;
}
